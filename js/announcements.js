$(function(){
    var $yearFilter = $('#announcements ._yearFilter').eq(0);
    var $list = $('#announcements ._list').eq(0);
    var $page = $('#announcements ._pagination').eq(0);

    var data = announcements.data;
    var pageSize = announcements.pageSize;
    var yearArray = [];

    var makeYearItem = function (year) {
        return $('<div class="_yearItem">'+ year +'</div>').data('year', year).on('click', function(){
            $(this).siblings().removeClass('active');
            $(this).addClass('active');
            makeList(year);
        });
    }

    var makeListItemWrap= function (data) {
        return $('<div class="_listItemWrap"></div>');
    }

    var makeListItem = function (data) {
        var year = $.trim(data.date.split('-')[0]);
        if (yearArray.indexOf(year) == -1) {
            yearArray.push(year);
        }
        return $('<div class="_listItem">'+
                    '<p class="_title" title="'+ data.title +'">'+data.title+'</p>'+
                    '<p class="_date">'+data.date+'</p>'+
                    '<div class="_buttons">'+
                        '<a href="'+data.cn+'" target="_blank"><img class="_icon_1" src="https://pmt8e84ed-pic16.websiteonline.cn/upload/7psq.png"/><img class="_icon_2" src="https://pmt8e84ed-pic16.websiteonline.cn/upload/zuka.png"/>中文下载</a>'+
                        '<a href="'+data.en+'" target="_blank"><img class="_icon_1" src="https://pmt8e84ed-pic16.websiteonline.cn/upload/7psq.png"/><img class="_icon_2" src="https://pmt8e84ed-pic16.websiteonline.cn/upload/zuka.png"/>英文下载</a>'+
                    '</div>'+
                '</div>')
    }

    var makePageItem = function (page) {
        return $('<div class="_pageItem">'+(page + 1)+'</div>').data('page', page).on('click', function(){
            setPage($(this).data('page'));
        });
    }

    var setPage = function (page) {
        $($list).find('._listItemWrap').removeClass('show');
        $($list).find('._listItemWrap').eq(page).addClass('show');
        $($page).find('._pageItem').removeClass('active');
        $($page).find('._pageItem').eq(page).addClass('active');

        if ( $($page).find('._pageItem').length > 15) {
            resetPage(page)
        }
    }

    var resetPage = function (page) {
        console.log(page);
        $($page).find('._ellipsis').remove();
        var pageItems = $($page).find('._pageItem').removeClass('hide');
        var length = pageItems.length;
        if (page < 7) {
            $($page).find('._pageItem').eq(7).after($('<div class="_ellipsis">...</div>'));
            for(var i = 8 ; i < length - 2; i++) {
                $($page).find('._pageItem').eq(i).addClass('hide');
            }
        } else if (page >= 7 && page < length - 7) {
            $($page).find('._pageItem').eq(1).after($('<div class="_ellipsis">...</div>'));
            $($page).find('._pageItem').eq(page + 2).after($('<div class="_ellipsis">...</div>'));
            for(var i = 2 ; i < page - 3; i++) {
                $($page).find('._pageItem').eq(i).addClass('hide');
            }
            for(var i = page + 3 ; i < length - 2; i++) {
                $($page).find('._pageItem').eq(i).addClass('hide');
            }
        } else if (page >= length - 7) {
            $($page).find('._pageItem').eq(1).after($('<div class="_ellipsis">...</div>'));
            for(var i = 2 ; i < length - 8; i++) {
                $($page).find('._pageItem').eq(i).addClass('hide');
            }
        }
    }

    var makeList = function (year) {
        $($list).empty();
        $($page).empty();
        let myData = []
        if (year !== '全部') {
            myData = data.filter(function(item){
                var itemYear = item.date.split('-')[0];
                return itemYear === year;
            })
        } else {
            myData = data;
        }
        var pageLength = Math.ceil(myData.length / pageSize);
        for(var i = 0 ; i < pageLength ; i++) {
            var $listWrap = makeListItemWrap();
            for (var j = i * pageSize ; j < (i + 1) * pageSize ; j++) {
                if (j === myData.length) break;
                var item = myData[j];
                var $listItem = makeListItem(item);
                $($listWrap).append($listItem);
            }
            $($list).append($listWrap);
    
            var $pageItem = makePageItem(i);
            $($page).append($pageItem);
        }
        setPage(announcements.page);
    }

    var makeYearFilter = function () {
        var $year = makeYearItem('全部');
        $($yearFilter).append($year);
        for(var i = 0 ; i < yearArray.length ; i++) {
            var $year = makeYearItem(yearArray[i]);
            $($yearFilter).append($year);
        }
    }


    makeList('全部');
    makeYearFilter();
    $($yearFilter).find('._yearItem').eq(0).addClass('active');
});




