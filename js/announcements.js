$(function(){
    var $list = $('#announcements ._list').eq(0);
    var $page = $('#announcements ._pagination').eq(0);

    var data = announcements.data;
    var pageSize = announcements.pageSize;

    var makeListItemWrap= function (data) {
        return $('<div class="_listItemWrap"></div>');
    }

    var makeListItem = function (data) {
        return $('<div class="_listItem">'+
                    '<p class="_title">'+data.title+'</p>'+
                    '<p class="_date">'+data.date+'</p>'+
                    '<div class="_buttons">'+
                        '<a href="'+data.cn+'" target="_blank"><img class="_icon_1" src="https://pmt8e84ed-pic16.websiteonline.cn/upload/7psq.png"/><img class="_icon_2" src="https://pmt8e84ed-pic16.websiteonline.cn/upload/zuka.png"/>中文下载</a>'+
                        '<a href="'+data.en+'" target="_blank"><img class="_icon_1" src="https://pmt8e84ed-pic16.websiteonline.cn/upload/7psq.png"/><img class="_icon_2" src="https://pmt8e84ed-pic16.websiteonline.cn/upload/zuka.png"/>英文下载</a>'+
                    '</div>'+
                '</div>')
    }

    var makePageItem = function (page) {
        return $('<div class="_pageItem">'+(page + 1)+'</div>').data('page', page).on('click', function(){
            setPage($(this).data('page'));
        });
    }

    var setPage = function (page) {
        $($list).find('._listItemWrap').removeClass('show');
        $($list).find('._listItemWrap').eq(page).addClass('show');
        $($page).find('._pageItem').removeClass('active');
        $($page).find('._pageItem').eq(page).addClass('active');
    }

    var pageLength = Math.ceil(data.length / pageSize);
    for(var i = 0 ; i < pageLength ; i++) {
        var $listWrap = makeListItemWrap();
        for (var j = i * pageSize ; j < (i + 1) * pageSize ; j++) {
            if (j === data.length - 1) break;
            var item = data[j];
            var $listItem = makeListItem(item);
            $($listWrap).append($listItem);
        }
        $($list).append($listWrap);

        var $pageItem = makePageItem(i);
        $($page).append($pageItem);
    }

    setPage(announcements.page);
});